 template<typename T, int SIZE >
 struct MyQueue
 {
    T arr[SIZE];
    int pos{0};

    void push(const T& value) {
        if (pos < 1000) {
            arr[pos++] = value;
        }
    }

    void pop() {
        if (!empty()) {
            for (int i = 0; i < pos - 1; ++i) {
                arr[i] = arr[i + 1];
            }
            pos--;
        }
    }

    T& front() {
        return arr[0];
    }

    bool empty() const {
        return pos == 0;
    }

    void print() const {
        for (int i = 0; i < pos; ++i) {
            cout << arr[i] << " ";
        }
        cout << endl;
    }

    template<typename Type>
    void sum_and_addd(Type a, Type b)
    {
        arr[pos++] == a + b; 
    }
 };

 int main(void)
 {
    MyQueue<string,1000> q_str; /* The size must be determine at the compilation time */

    q_str.push("Mostafa");
    q_str.push("Saad");

    q_str.print();


    MyQueue<int,1000> q_dob; 
    q_dob.push(3);
    q_dob.push(3);
    q_dob.sum_and_addd<double>(2.4,3.2);
    q_dob.print();


 }