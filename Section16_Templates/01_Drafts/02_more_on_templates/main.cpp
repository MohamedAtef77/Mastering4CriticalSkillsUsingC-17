template <class Type1, class Type2>

Type1 sum(Type1 a, Type2 b)
{
    Type1 r = a + b ; 
    return r;
}
 int main()
{

    cout << sum(1,10) << endl; 
    cout << sum(1,10.5) << endl; 
    cout << sum(1.2,10.5) << endl; 
    cout << sum<int,int>(1.2,10) << endl;
    cout << sum('A',1) << endl; /* A*/
    cout << sum(1,'A') << endl; /* 66 */

    cout << sum(string("I'm "),string("Iron Man. ")) << endl;
    return 0;
}
