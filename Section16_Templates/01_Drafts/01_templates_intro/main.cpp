template <typename Type>

Type our_max(Type a, Type b)
{
    return a > b ? a : b ; 
}
 int main()
{
    cout << our_max(2,5) << endl;
    cout << our_max<int>(2,5) << endl;
    cout << our_max<double>(2.5,5.4) << endl;
    cout << our_max('A','X') << endl;
    cout << our_max<int>(2,5.4) << endl;
    cout << our_max<double>(2.5,4) << endl;

    return 0;
}
