template <class T>
T multiply(T a, int b)
{ 
    return a * b; 
}

string multiply(const string& a, int times)
{
    string result{""}; 
    for(int i = 0; i < times; ++i)
    {
        result += a;
    }

    return result;
}

int main()
{
    cout << multiply(4,5) << endl;
    cout << multiply(string("aa"),3) << endl;
    return 0;
}
