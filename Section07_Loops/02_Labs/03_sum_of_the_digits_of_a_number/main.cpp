int main(void)
{
    int num{0};
    int digits_count{0};

    cout << "Enter a number: ";
    cin >> num; 
    if(num == 0)
    {
        digits_count = 1 ; 
    }
    else 
    {
        while (num > 0)
        {
            digits_count++;
            num /= 10;
        }
    }


    cout << "The count of the digits is: " << digits_count<< endl;




}