int main(void)
{
    int x{1};
    int sum{0};

    while(x <= 5)
    {
        sum += x;
        x++;
    }
    cout << sum << endl;

    x = 5;
    sum = 0;

    while(x > 0)
    {
        sum += x;
        x--;
    }
    cout << sum << endl;
}