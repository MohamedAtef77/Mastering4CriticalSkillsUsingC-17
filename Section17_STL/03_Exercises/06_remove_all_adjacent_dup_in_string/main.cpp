string remove_duplicates(string st)
{
    stack<char> my_stack;
    string result{""};
    auto it = st.begin();


    reverse(st.begin(),st.end());


    while(it != st.end())
    {

        if ((my_stack.empty()) || (*it != my_stack.top())) /* Handle inserting unique elements */
        {
            my_stack.push(*it);
            ++it;
        }
        else /*Handle duplication */
        {
            while(it != st.end() && *it == my_stack.top())
            {
                ++it; /* Loop until the next unique element */
            }

            /* Pop the inserted character which happens to have duplicates */
            my_stack.pop();
        }
    }

    while(!my_stack.empty())
    {
        result += my_stack.top();
        my_stack.pop();
    }
    return result;
}

