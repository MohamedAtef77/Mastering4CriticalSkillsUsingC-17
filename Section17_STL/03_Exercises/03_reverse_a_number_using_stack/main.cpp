struct OurStack
{
    queue<int> q;
    queue<int> spare_q; 
    int temp{0};

    void push(int val)
    {
        /* Empty all of the contents in the other queue */
        while(!q.empty())
        {
            temp = q.front();
            q.pop();
            spare_q.push(temp);
        }

        /* Insert the element at front */
        q.push(val);

        /* Push the elements after wards */
        while(!spare_q.empty())
        {
            temp = spare_q.front();
            spare_q.pop();
            q.push(temp);
        }
    }
    void pop()
    {
        if (!q.empty())
            q.pop();
    }

    int top()
    {
        return q.front();
    }

    bool empty()
    {
        return q.empty();
    }
};





int main()
{

    OurStack s;
    s.push(1);
    s.push(2);
    s.push(3);

    while (!s.empty())
        cout << s.top() << " ", s.pop();

    return 0;
}