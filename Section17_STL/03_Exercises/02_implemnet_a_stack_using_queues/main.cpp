 int reverse_a_number(int num)
 {
    stack<int> st;
    int reversed_num{0};
    int weight{1};


    if(num == 0)
    {
        return 0;
    }


    while(num > 0)
    {
        st.push(num % 10);
        num /= 10;
    }

    while(! st.empty())
    {

        reversed_num += st.top() * weight;
        weight *= 10;

        st.pop();
    }

    return reversed_num;

 }
