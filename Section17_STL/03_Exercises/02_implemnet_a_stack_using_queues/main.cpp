struct OurStack {
    queue<int> q;

    void push(int val) {
        // Get the current size before pushing
        int s = q.size();
        
        // Add the new element to the back
        q.push(val);

        // Rotate the queue: move all previous elements to the back
        for (int i = 0; i < s; i++) {
            q.push(q.front());
            q.pop();
        }
    }

    void pop() {
        if (!q.empty()) q.pop();
    }

    int top() {
        return q.empty() ? -1 : q.front();
    }

    bool empty() {
        return q.empty();
    }
};

int main(int argc, char const *argv[])
{
    OurStack s;
    s.push(10);
    s.push(20);
    s.push(30);

    while (!s.empty()) {
        cout << s.top() << " ";
        s.pop();
    }
    cout << endl;

    return 0;
}
