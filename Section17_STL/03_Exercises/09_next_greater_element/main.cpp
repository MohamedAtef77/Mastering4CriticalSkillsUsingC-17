#include <vector>
#include <cstdlib>
using namespace std;
vector<int> nextGreaterElement(const vector<int> &a)
{
    int n = (int)a.size();
    vector<int> ans(n, -1);
    stack<int> st; // stores indices

    for (int i = 0; i < n; i++)
    {
        while (!st.empty() && a[i] > a[st.top()])
        {
            ans[st.top()] = a[i];
            st.pop();
        }
        st.push(i);
    }
    return ans;
}

int main(void)
{
    vector<int> a{4, 5, 2, 25, 7, 8};
    vector<int> b{13, 7, 6, 12};
    vector<int> c{1, 1, 1};

    auto print_result = [](const vector<int> &v) {
        vector<int> res = nextGreaterElement(v);
        for (size_t i = 0; i < v.size(); ++i)
        {
            cout << v[i] << " -> " << res[i] << "\n";
        }
        cout << "----\n";
    };

    print_result(a);
    print_result(b);
    print_result(c);
}
