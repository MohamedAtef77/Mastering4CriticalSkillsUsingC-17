void reverse_queue(queue<int> &q)
{
    stack<int>st; 

    while(!q.empty())
    {
        st.push(q.front());
        q.pop();
    }

    while(!st.empty())
    {
        q.push(st.top());
        st.pop();
        
    }
}

 int main(void)
{
    queue<int> q;
    for (int i = 1; i <= 5; ++i)
        q.push(i);

    cout << "Original queue: 1 2 3 4 5" << endl;
    reverse_queue(q);

    cout << "Reversed queue: ";
    while (!q.empty()) {
        cout << q.front() << " ";
        q.pop();
    }
    cout << endl;

    return 0;
}