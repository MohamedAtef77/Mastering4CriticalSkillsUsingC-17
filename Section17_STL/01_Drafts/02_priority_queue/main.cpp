int main(int argc, char const *argv[])
{
    priority_queue<int> q; 

    q.push(10);

    cout << q.top() << endl;

    q.push(20);

    cout << q.top() << endl;


    q.push(15);
    cout << q.top() << endl;


    /* Print the queue*/

    while(!q.empty())
    {
        cout << q.top() << " ";
        q.pop();
    }

    cout << endl;



    return 0;
}
