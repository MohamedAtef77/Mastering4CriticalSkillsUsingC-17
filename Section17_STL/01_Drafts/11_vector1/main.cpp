int main(int argc, char const *argv[])
{
    vector<int> v1; /* A vector is simply an array which can be expanded */

    v1.push_back(30);
    v1.push_back(10);
    v1.push_back(20);


    for(int i = 0 ; i < v1.size(); ++i)
    {
        cout << v1[i] << " ";
    }

    cout << endl; 

    vector<int> v2(5,7);

    for(auto &val: v2)
    {
        cout << val << " " ;

    }
    cout << endl;

    cout << v2.at(0) << endl; 
}
