int main(int argc, char const *argv[])
{
    string test{"hi abc abc abc abc"};


    cout << test.substr(3) << endl; 

    cout << test.substr(3,5) << endl; 


    cout << test.find("abc") << endl; /* Finds the first occurenece of a substring */

    cout << "Mostafa at: " << test.find("Mostafa") << endl;

    cout << "Mostafa at: " << (int) test.find("Mostafa") << endl;

    // Functions that searches for a CHARACTER from a given string
    cout << test.find_last_of("cab") << "\n";      // 17
    cout << test.find_first_of("aic") << "\n";     // 1
    cout << test.find_first_of("aic", 4) << "\n";  // 5
    cout << test.find_first_not_of("aic") << "\n"; // 0


    test.replace(4,3,"x");

    cout << test << endl; 

    
    return 0;
}
