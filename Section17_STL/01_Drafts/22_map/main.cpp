void print(const map<string,int> &mp)
{
    for(const pair<string,int> &pr: mp)
    {
        cout << pr.first << " " << pr.second << endl; 
    }
}
int main(int argc, char const *argv[])
{
    map<string,int> my_map; 


    my_map["mostafa"] = 10;
    my_map["saad"] = 20;
    my_map["mostafa"] = 30; 
    my_map["Mostafa"] = 40; 


    print(my_map);

    /* You typically search by key */
    auto it = my_map.find("Saad");
    if(it != my_map.end())
    {
        my_map.erase(it);
    }


    print(my_map);


    cout << my_map.count("Mostafa") << endl;

    

    return 0;
}
