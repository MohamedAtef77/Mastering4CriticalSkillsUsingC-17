struct employee
{
    int num1{0};
    int num2{0};
    string str;

    /* Constructor */
    employee(int a, int b, string name)
    {
        num1 = a ; 
        num2 = b;
        str = name;  
    }


    /* Operator overloading of < to makae the implementation of sets over this struct feasiible */

    bool operator <(const employee &rhs) const
    {
        return tie(num1, str, num2) < tie(rhs.num1, rhs.str, rhs.num2);
    }

    void print() const
    {
        cout << "ID1: " << num1 << "\t ID2: " << num2 << "\t Name: " << str << endl;
    }

};



int main(int argc, char const *argv[])
{
    /* code */
    set<employee> s; 

    s.insert(employee(10,35,"mostafa"));
    s.insert(employee(7,15,"ali"));
    s.insert(employee(10,20,"mostafa"));

    for(const auto &x: s)
    {
        x.print();
    }
    return 0;
}
