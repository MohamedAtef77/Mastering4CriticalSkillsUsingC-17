struct Employee
{
    string name; 
    int salaray; 
};

 int main(void)
{
   Employee emp{"Mohamed", 20};

    /* Bind by value */
   auto [str1, sal1] = emp; 

   cout << str1 << " " << sal1 << endl; 


   /* Bind by reference */
   auto &[str2, sal2] = emp;

   sal2 += 20; 

   cout << str1 << " " << sal2 << endl; 

}