int main(void)
{
    int N{0};
    int data[100];
    int sum{0};

    priority_queue<int> sums;
    int temp_element{0};
    cout << "Enter the number of elements: ";
    cin >> N; 

    /* Read the data */
    for(auto i{0}; i != N; ++i)
    {
        cin >> data[i];
    }

    for(auto i{0}; i != N; ++i)
    {
        if((i + 3) <= N)
        {
            sum = data[i];
            for(auto j{i + 1}; j != (i+3); ++j)
            {
                if (data[j] > sum)
                    sum = data[j];
            }

            sums.push(sum);
            cout << "The max is: " << sum << endl; 
        }
    }

    cout << "The maximum value is: " << sums.top() << endl; 

}