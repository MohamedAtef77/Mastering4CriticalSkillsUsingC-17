void print_row_recursive(int len)
{

    if(len <=0)
    {
        cout << endl;
    }
    else 
    {
        cout << "*";
        print_row_recursive(len -1);
    }
}
void print_triange_v1_recursive(int n, int start = 1)
{
    if(start > n)
    {
        return;
    }
    print_row_recursive(start);
    print_triange_v1_recursive(n,start + 1);
}

