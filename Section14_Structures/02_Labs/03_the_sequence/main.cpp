/**********************************************************************************************************************
 *  FILE INCLUSIONS
 *********************************************************************************************************************/
#include <iostream>
#include <climits>

/**********************************************************************************************************************
 *  USED NAMESPACES
 *********************************************************************************************************************/
using namespace std;

/**********************************************************************************************************************
 *  GLOBAL FUNCTION DEFINITIONS
 *********************************************************************************************************************/
void sequence(int n)
{
    cout << n << " ";
    if(n <= 1)
    {
        cout << endl;
        return;
    }
    else if(!(n % 2)) /* Even */
    {
        sequence(n/2);
    }
    else 
    {
        sequence(3 *n + 1);
    }
}

/**********************************************************************************************************************
 *  MAIN FUNCTION
 *********************************************************************************************************************/
int main()
{
    sequence(5);
    sequence(6);
    sequence(7);

    return 0;
}
