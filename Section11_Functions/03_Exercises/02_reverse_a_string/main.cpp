string reverse_string(const string &original_str)
{
    string new_str;
    int start = 0; 
    int end = original_str.length() - 1; 

    while(end >= 0)
    {
        new_str += original_str[end];

        start++;
        end--;
    }


    return new_str;
}
int main(int argc, char const *argv[])
{
    cout << reverse_string("Mohamed") << endl;


    return 0;
}
