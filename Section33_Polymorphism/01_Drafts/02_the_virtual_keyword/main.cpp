 class Person 
 {
 public:
    virtual void print(void)
    {
        cout << "You are inside an object of class Person" << endl; 
    }

    virtual ~Person()
    {
        cout << "This is the destructor of Person Class " << endl; 
    }

 };

 class Student : public Person
 {
    public:
    void print(void)
    {
        cout << "You are inside an object of class Student" << endl;
    }

    ~Student()
    {
        cout << "This is the destructor of Student Class " << endl;
    }
 };
int main(void)
{
    Person p1; 

    Person *p1_ptr = &p1;

    Student s1; 

    Person *s1_ptr = &s1; 
    Person *s2_ptr = new Student; 


    s1_ptr->print();

    s2_ptr->print();

    cout << "Now the program is about to close " << endl; 

    delete s2_ptr;
}