int main(int argc, char const *argv[])
{
    bool append = true; 


    auto status = ios::in | ios::out | ios::app; 


    if(!append)
    {
        status = ios::in | ios::out | ios::trunc;
    }

    string path = "names.txt";

    // Initialize the file stream object to open the file at 'path' using the combined 
    // open-mode flags stored in 'status' (allowing for simultaneous reading and writing).
    fstream file_handler{path.c_str(),status}; 


    if(file_handler.fail())
    {
        cout << "Can't open the file " << endl; 


        return 0;
    }


    string first, second; 


    file_handler >> first >> second; 


    cout << first << " " << second << endl; 

    // Clear the stream state (EOF bit) after reading to the end of the file
    file_handler.clear();

    file_handler << "Cairo"; 
    



}
