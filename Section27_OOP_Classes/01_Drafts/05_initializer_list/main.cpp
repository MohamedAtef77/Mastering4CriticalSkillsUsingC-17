class Adder
{
private:
    int total_sum{};
public:
    Adder()
    {

    }
    Adder(const initializer_list<int> &lst)
    {
        Add(lst);
    }
    int Add(const initializer_list<int> &lst)
    {
        int sum{};
        for(auto &value: lst)
        {
            sum += value; 
        }

        total_sum += sum;

        return sum;
    }
    int GetCurrentSum() const
    {
        return total_sum;
    }

};
 int main(int argc, char const *argv[])
{
    /* code */
    Adder adder{1,2,3}; /* Make use of the initializer list syntax */

    cout << adder.GetCurrentSum() << endl; 

    adder.Add({4,5}); /* Now you are passing a const initializer_list<int> & */

    cout << adder.GetCurrentSum() << endl; 


    return 0;
}
