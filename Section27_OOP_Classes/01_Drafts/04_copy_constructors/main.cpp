class myNumber
{
private:
    int *val1;
    int val2;

public:
    myNumber(int x = 3, int y = 5)
    {
        cout << "Normal Constructor" << endl; 
        val1 = new int; 
        *val1 = x; 
        val2 = y; 
    }

    myNumber(const myNumber &num)
    {
        cout << "Copy Constructor" << endl;
        val1 = new int;
        *val1 = *(num.val1);
        val2 = num.val2;
    }

    void PrintValueAndAddress()
    {
        cout << "val1 = " << *val1 << " (address: " << val1 << "), val2 = " << val2 << endl;
    }
};
int main(int argc, char const *argv[])
{
   myNumber a;
   myNumber b(a);
   a.PrintValueAndAddress();
   b.PrintValueAndAddress();
}
