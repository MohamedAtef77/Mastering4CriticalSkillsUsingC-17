class myVector
{
private:
    int *arr; 
    int len = 100;
public:
    myVector(int len, int default_value): len(len)
    {
        arr = new int [len];
        for (auto i{0}; i != len; ++i)
        {
            arr[i] = default_value;
        }
    }

    ~myVector()
    {
        delete[] arr; 
    }

    int Get(int pos)
    {
        return arr[pos];

        // or return this->arr[i]
    }
};
int main(int argc, char const *argv[])
{
    /* code */
    myVector v(10,12345);

    cout << v.Get(5) << endl; // 12345
    return 0;
}
