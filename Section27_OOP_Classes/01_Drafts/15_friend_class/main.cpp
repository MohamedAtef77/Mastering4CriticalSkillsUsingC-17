class A
{
private:
    int a1_private{10};
    int a2_private{20};
    void HiddenA(void)
    {
        cout << "I'm hidden" << endl; 
    }

public: 
    friend class B; /* B doesn't even need to be defined */

};
class B
{
public: 
    void AccessA1(A& x)
    {
        cout << x.a1_private << endl;
        x.HiddenA();
    }
    void AccessA2(A& x)
    {
        cout << x.a2_private << endl;
        x.HiddenA();
    }
};
int main() {

    A a; 
    B b; 

    b.AccessA1(a); 
    b.AccessA2(a);


    return 0; 
}
