/**********************************************************************************************************************
 *  FILE INCLUSIONS
 *********************************************************************************************************************/
#include <iostream>
using namespace std;
/**********************************************************************************************************************
 *  GLOBAL CONSTANTS & VARIABLES
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  TYPE DEFINITIONS & STRUCTS/CLASSES
 *********************************************************************************************************************/
class Test {
public:
    static int x;  // Declare: shared among all objects
};

int Test::x = 0;  // Define outside

/**********************************************************************************************************************
 *  LOCAL & GLOBAL MACROS
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  GLOBAL FUNCTION DECLARATIONS
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  GLOBAL FUNCTION DEFINITIONS
 *********************************************************************************************************************/
void f() {
    Test::x += 10;  // Same x
}

/**********************************************************************************************************************
 *  MAIN FUNCTION
 *********************************************************************************************************************/
int main() {
    Test::x += 1;  // Access

    f();

    cout << Test::x << "\n";

    return 0;
}
