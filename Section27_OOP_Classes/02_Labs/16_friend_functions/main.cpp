/**********************************************************************************************************************
 *  FILE INCLUSIONS
 *********************************************************************************************************************/
#include <iostream>
using namespace std;
/**********************************************************************************************************************
 *  GLOBAL CONSTANTS & VARIABLES
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  TYPE DEFINITIONS & STRUCTS/CLASSES
 *********************************************************************************************************************/
class A
{
private:
    int a1_private{10};
    int a2_private{20};
    void HiddenA(void)
    {
        cout << "I'm hidden" << endl;
    }

public:
    friend void AccessA1(A& x);  // Standalone friend function
    friend void AccessA2(A& x);  // Standalone friend function
};

/**********************************************************************************************************************
 *  LOCAL & GLOBAL MACROS
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  GLOBAL FUNCTION DECLARATIONS
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  GLOBAL FUNCTION DEFINITIONS
 *********************************************************************************************************************/
void AccessA1(A& x)  // Not a member of any class
{
    cout << x.a1_private << endl;
    x.HiddenA();
}

void AccessA2(A& x)  // Not a member of any class
{
    cout << x.a2_private << endl;
    x.HiddenA();
}

/**********************************************************************************************************************
 *  MAIN FUNCTION
 *********************************************************************************************************************/
int main() {

    A a;

    AccessA1(a);
    AccessA2(a);

    return 0;
}
